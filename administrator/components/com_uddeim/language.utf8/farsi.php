function udde_pdate($format, $when="now", $persianNumber=0) {
	///chosse your timezone
	$TZhours = 0;
	$TZminute = 0;
	$need = "";
	$result1 = "";
	$result = "";
	if ($when=="now") {
		$year = date("Y");
		$month = date("m");
		$day = date("d");
		list( $Dyear, $Dmonth, $Dday ) = udde_gregorian2persian($year, $month, $day);
		$when = mktime(date("H")+$TZhours, date("i")+$TZminute, date("s"), date("m"), date("d"), date("Y"));
	} else {
		//$when = 0;
		$when += $TZhours*3600 + $TZminute*60;
		$date = date("Y-m-d",$when);
		list( $year, $month, $day ) = preg_split ( '/-/', $date );
		list( $Dyear, $Dmonth, $Dday ) = udde_gregorian2persian($year, $month, $day);
	}

	$need = $when;
	$year = date("Y", $need);
	$month = date("m", $need);
	$day = date("d", $need);
	$i = 0;
	$subtype = "";
	$subtypetemp = "";
	list( $Dyear, $Dmonth, $Dday ) = udde_gregorian2persian($year, $month, $day);
	while ($i<strlen($format)) {
		$subtype = substr($format,$i,1);
		if ($subtypetemp=="\\") {
			$result .= $subtype;
			$i++;
			continue;
		}

		switch ($subtype) {
			case "A":
				$result1 = date("a", $need);
				if ($result1=="pm") $result .=  "&#1576;&#1593;&#1583;&#1575;&#1586;&#1592;&#1607;&#1585;";	// "بعدازظهر"
				else $result .= "&#1602;&#1576;&#1604;&#8207;&#1575;&#1586;&#1592;&#1607;&#1585;";			// "قبل‏ازظهر"
				break;

			case "a":
				$result1 = date("a", $need);
				if ($result1=="pm") $result .=  "&#1576;&#46;&#1592;";	// "ب.ظ"
				else $result .= "&#1602;&#46;&#1592;";					// "ق.ظ"
				break;
			case "d":
				if ($Dday<10)$result1 = "0".$Dday;
				else $result1=$Dday;
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "D":
				$result1 = date("D", $need);
				if ($result1=="Thu") $result1 = "&#1662;";		// "پ"
				elseif ($result1=="Sat") $result1 = "&#1588;";	// "ش"
				elseif ($result1=="Sun") $result1 = "&#1609;";	// "ى"
				elseif ($result1=="Mon") $result1 = "&#1583;";	// "د"
				elseif ($result1=="Tue") $result1 = "&#1587;";	// "س"
				elseif ($result1=="Wed") $result1 = "&#1670;";	// "چ"
				elseif ($result1=="Thu") $result1 = "&#1662;";	// "پ"
				elseif ($result1=="Fri") $result1 = "&#1580;";	// "ج"
				$result .= $result1;
				break;
			case"F":
				$result .= udde_pmonthname($Dmonth);
				break;
			case "g":
				$result1 = date("g", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "G":
				$result1 = date("G", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
				case "h":
				$result1 = date("h", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "H":
				$result1 = date("H", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "i":
				$result1 = date("i", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "j":
				$result1=$Dday;
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "l":
				$result1 = date("l", $need);
				if ($result1=="Saturday") $result1 = "&#1588;&#1606;&#1576;&#1607;";	// "شنبه"
				elseif ($result1=="Sunday") $result1 = "&#1610;&#1603;&#1588;&#1606;&#1576;&#1607;";	// "يكشنبه"
				elseif ($result1=="Monday") $result1 = "&#1583;&#1608;&#1588;&#1606;&#1576;&#1607;";	// "دوشنبه"
				elseif ($result1=="Tuesday") $result1 = "&#1587;&#1607;&#32;&#1588;&#1606;&#1576;&#1607;";	// "سه شنبه"
				elseif ($result1=="Wednesday") $result1 = "&#1670;&#1607;&#1575;&#1585;&#1588;&#1606;&#1576;&#1607;";	// "چهارشنبه"
				elseif ($result1=="Thursday") $result1 = "&#1662;&#1606;&#1580;&#1588;&#1606;&#1576;&#1607;";	// "پنجشنبه"
				elseif ($result1=="Friday") $result1 = "&#1580;&#1605;&#1593;&#1607;";	// "جمعه"
				$result .= $result1;
				break;
			case "m":
				if ($Dmonth<10) $result1 = "0".$Dmonth;
				else $result1=$Dmonth;
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "M":
				$result .= udde_psmonthname($Dmonth);
				break;
			case "n":
				$result1=$Dmonth;
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "s":
				$result1 = date("s", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "S":
				$result .= "&#1575;&#1605;";
				break;
			case "t":
				$result .= udde_plastday ($month, $day, $year);
				break;
			case "w":
				$result1 = date("w", $need);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "y":
				$result1=substr($Dyear,2,4);
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "Y":
				$result1=$Dyear;
				if ($persianNumber==1) $result .= udde_convertnumber2farsi($result1);
				else $result .= $result1;
				break;
			case "U" :
				$result .= mktime();
				break;
			case "Z" :
				$result .= udde_pdaysofyear($Dmonth,$Dday,$Dyear);
				break;
			case "L" :
				list( $tmp_year, $tmp_month, $tmp_day ) = udde_persian2gregorian(1384, 12, 1);
				if (udde_plastday($tmp_month, $tmp_day, $tmp_year)=="31")
					$result .= "1";
				else
					$result .= "0";
				break;
			default:
				$result .= $subtype;
		}
		$subtypetemp = substr($format,$i,1);
		$i++;
	}
	return $result;
}

function udde_pmktime($hour, $minute, $second, $jmonth, $jday, $jyear) {
	list( $year, $month, $day ) = udde_persian2gregorian($jyear, $jmonth, $jday);
	$i = mktime($hour, $minute, $second, $month, $day, $year);	
	return $i;
}

// find day begining of month 
function udde_pmstart($month, $day, $year) {
	list( $jyear, $jmonth, $jday ) = udde_gregorian2persian($year, $month, $day);
	list( $year, $month, $day ) = udde_persian2gregorian($jyear, $jmonth, "1");
	$timestamp=mktime(0, 0, 0, $month, $day, $year);
	return date("w", $timestamp);
}

// find number of days in this month
function udde_plastday($month, $day, $year) {
	$Dday2 = "";
	$jdate2  = "";
	$lastdayen = date("d",mktime(0, 0, 0, $month+1, 0, $year));
	list( $Dyear, $Dmonth, $Dday ) = udde_gregorian2persian($year, $month, $day);
	$lastdatep = $Dday;
	$Dday = $Dday2;
	while ($Dday2!="1") {
		if ($day<$lastdayen) {
			$day++;
			list( $Dyear, $Dmonth, $Dday2 ) = udde_gregorian2persian($year, $month, $day);
			if ($jdate2=="1") break;
			if ($jdate2!="1") $lastdatep++;
		} else {
			$day = 0;
			$month++;
			if ($month==13) {
				$month="1";
				$year++;
			}
		}
	}
	return $lastdatep-1;
}

//Find days in this year untile now
function udde_pdaysofyear($Dmonth, $Dday, $Dyear) {
	$year = "";
	$month = "";
	$year = "";
	$result = "";
	if ($Dmonth=="01")
		return $Dday;
	for ($i=1; $i<$Dmonth || $i==12; $i++) {
		list( $year, $month, $day ) = udde_persian2gregorian($Dyear, $i, "1");
		$result+=udde_plastday($month, $day, $year);
	}
	return $result + $Dday;
}

