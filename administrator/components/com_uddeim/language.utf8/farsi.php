//converts the numbers into the persian's number
function udde_convertnumber2farsi($srting) {
	$num0="&#1776;";	// "۰"
	$num1="&#1777;";	// "۱"
	$num2="&#1778;";	// "۲"
	$num3="&#1779;";	// "۳"
	$num4="&#1780;";	// "۴"
	$num5="&#1781;";	// "۵"
	$num6="&#1782;";	// "۶"
	$num7="&#1783;";	// "۷"
	$num8="&#1784;";	// "۸"
	$num9="&#1785;";	// "۹"
	$stringtemp = "";
	$len = strlen($srting);
	for($sub=0; $sub<$len; $sub++) {
		if (substr($srting,$sub,1)=="0") $stringtemp .= $num0;
		elseif (substr($srting,$sub,1)=="1") $stringtemp .= $num1;
		elseif (substr($srting,$sub,1)=="2") $stringtemp .= $num2;
		elseif (substr($srting,$sub,1)=="3") $stringtemp .= $num3;
		elseif (substr($srting,$sub,1)=="4") $stringtemp .= $num4;
		elseif (substr($srting,$sub,1)=="5") $stringtemp .= $num5;
		elseif (substr($srting,$sub,1)=="6") $stringtemp .= $num6;
		elseif (substr($srting,$sub,1)=="7") $stringtemp .= $num7;
		elseif (substr($srting,$sub,1)=="8") $stringtemp .= $num8;
		elseif (substr($srting,$sub,1)=="9") $stringtemp .= $num9;
		else $stringtemp .= substr($srting,$sub,1);
	}
	return $stringtemp;
}

function udde_pdiv($a,$b) {
	return (int) ($a / $b);
}

function udde_gregorian2persian($g_y, $g_m, $g_d) {
	$g_days_in_month = array(31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31);
	$m_days_in_month = array(31, 31, 31, 31, 31, 31, 30, 30, 30, 30, 30, 29);
	$gy = $g_y-1600;
	$gm = $g_m-1;
	$gd = $g_d-1;
	$g_day_no = 365*$gy + udde_pdiv($gy+3,4) - udde_pdiv($gy+99,100) + udde_pdiv($gy+399,400);

	for ($i = 0; $i < $gm; ++$i)
		$g_day_no += $g_days_in_month[$i];
	if ($gm>1 && (($gy%4==0 && $gy%100!=0) || ($gy%400==0))) //* leap and after Feb
		$g_day_no++;
	$g_day_no += $gd;
	$m_day_no = $g_day_no-79;
	$j_np = udde_pdiv($m_day_no, 12053); // 12053 = 365*33 + 32/4 
	$m_day_no = $m_day_no % 12053;
	$jy = 979+33*$j_np+4*udde_pdiv($m_day_no,1461); // 1461 = 365*4 + 4/4
	$m_day_no %= 1461;
	if ($m_day_no >= 366) {
		$jy += udde_pdiv($m_day_no-1, 365);
		$m_day_no = ($m_day_no-1)%365;
	}
	for ($i = 0; $i < 11 && $m_day_no >= $m_days_in_month[$i]; ++$i)
		$m_day_no -= $m_days_in_month[$i];
	$jm = $i+1;
	$jd = $m_day_no+1;
	return array($jy, $jm, $jd);
}

function udde_persian2gregorian($m_y, $j_m, $m_d) {
	$g_days_in_month = array(31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31);
	$m_days_in_month = array(31, 31, 31, 31, 31, 31, 30, 30, 30, 30, 30, 29);
	$jy = $m_y-979;
	$jm = $j_m-1;
	$jd = $m_d-1;
	$m_day_no = 365*$jy + udde_pdiv($jy, 33)*8 + udde_pdiv($jy%33+3, 4);
	for ($i = 0; $i < $jm; ++$i)
		$m_day_no += $m_days_in_month[$i];
	$m_day_no += $jd;
	$g_day_no = $m_day_no+79;
	$gy = 1600 + 400*udde_pdiv($g_day_no, 146097); // 146097 = 365*400 + 400/4 - 400/100 + 400/400
	$g_day_no = $g_day_no % 146097;
	$leap = true;
	if ($g_day_no >= 36525) // 36525 = 365*100 + 100/4
	{
		$g_day_no--;
		$gy += 100*udde_pdiv($g_day_no,  36524); // 36524 = 365*100 + 100/4 - 100/100
		$g_day_no = $g_day_no % 36524;
		if ($g_day_no >= 365)
			$g_day_no++;
		else
			$leap = false;
	}
	$gy += 4*udde_pdiv($g_day_no, 1461); // 1461 = 365*4 + 4/4
	$g_day_no %= 1461;
	if ($g_day_no >= 366) {
		$leap = false;
		$g_day_no--;
		$gy += udde_pdiv($g_day_no, 365);
		$g_day_no = $g_day_no % 365;
	}
	for ($i = 0; $g_day_no >= $g_days_in_month[$i] + ($i == 1 && $leap); $i++)
		$g_day_no -= $g_days_in_month[$i] + ($i == 1 && $leap);
	$gm = $i+1;
	$gd = $g_day_no+1;
	return array($gy, $gm, $gd);
}
